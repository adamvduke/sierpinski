// Package config maintains constants that are used elsewhere in the sierpinski
// program.
package config

const (
	// DefaultBaseLength provides a default value for the length of a side of
	// a sierpinski triangle.
	DefaultBaseLength = 25

	// DefaultPointCount provides a default value for the number of points in a
	// sierpinski triangle.
	DefaultPointCount = 500

	// DefaultPointRadius provides a default value for the size of a point when
	// graphed.
	DefaultPointRadius = 1.0

	// DefaultPointRed provides a default red value for constructing a color.
	DefaultPointRed = 255

	// DefaultPointRed provides a default green value for constructing a color.
	DefaultPointGreen = 0

	// DefaultPointRed provides a default blue value for constructing a color.
	DefaultPointBlue = 0
)
